    private void AddButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddButtonActionPerformed
        // TODO add your handling code here:
        long passengerId = Long.parseLong(IDTextField5.getText());
        String flightId = FLIGHT.getItemAt(FLIGHT.getSelectedIndex());

        Passenger passenger = null;
        Flight flight = null;

        for (Passenger p : this.passengers) {
            if (p.getId() == passengerId) {
                passenger = p;
            }
        }

        for (Flight f : this.flights) {
            if (flightId.equals(f.getId())) {
                flight = f;
            }
        }
        PassengerManager manager = new PassengerManager();
        manager.addFlight(flight,passenger);
        manager.addPassenger(passenger, flight);
    }//GEN-LAST:event_AddButtonActionPerformed

